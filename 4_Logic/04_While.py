### while 반복문 ###

i = 1
while i < 5:
    print(i)
    i += 1

# vscode에서 디버깅 하는 방법

# 1. 일시정지를 하고 싶은 라인에 키보드 입력 커서를 위치시킨 후 F9(toggle break point)를 누른다.
# 2. 상위 메뉴의 Run > Start Debugging을 실행
# 3. Run and Debug(ctrl + shift + D)의 'VARIABLES' 탭의 Local에서 잡아주는 변수를 찹조.
# 3-1. (옵션)특별히 관찰하고 싶은 변수가 있다면 WATCH 탭에서 변수 이름 추가
# 4. Step over(F10)을 누르면서 한단계식 진행
