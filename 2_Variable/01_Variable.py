### 변수에 숫자 저장하는 방법 ###

var = 1  # var에 1을 단순 저장
print(var)  # 1

var = 2 + 2  # var에 2 + 2의 연산된 결과, 즉 4를 저장
print("결과2:", var)  # 결과2: 4

var = 17 / 3  # var에 17 ÷ 3, 즉 5.66666... 을 저장
print("결과3:", var)  # 결과3: 5.666666666666667

var = 17 // 3  # var에 17 ÷ 3 (나머지 제외), 즉 5를 저장
print("결과4:", var)  # 결과4: 5

var = 2**3  # var에 2 ^ 3(2의 3승 = 8)을 저장, 참고로 ^ 기호는 다른 연산이다.
print("결과5:", var)  # 결과5: 8
