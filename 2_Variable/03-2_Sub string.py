### 문자 쪼개기 ###

# +---+---+---+---+---+---+
# | P | y | t | h | o | n |
# +---+---+---+---+---+---+
# 0   1   2   3   4   5   6
# -6  -5  -4  -3  -2  -1

string1 = "Python"
print("출력1:", string1)  # Python

print("출력2:", string1[0])  # P

print("출력3:", string1[-2])  # o

print("출력4:", string1[1:3])  # yt

print("출력5:", string1[:4])  # Pyth

print("출력6:", string1[-2:])  # on
